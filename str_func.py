def func_upper_letters(text):
    return text.upper()

def func_title_letters(text):
    #Функция, которая делает заглавными первые буквы в словах из строки
    return text.title()


print(func_upper_letters("Маленькой ёлочке холодно зимой"))
print(func_title_letters("Маленькой ёлочке холодно зимой"))
